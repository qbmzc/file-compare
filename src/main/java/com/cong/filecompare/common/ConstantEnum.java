package com.cong.filecompare.common;

/**
 *  // Enum ConstantEnum is used to store constant values. It contains a single constant X-TOKEN with its value.
 * @author cong
 * @date 2023/2/1 17:23
 */

public enum ConstantEnum {
    X_TOKEN("X-TOKEN");

    final String value;

    ConstantEnum(String value) {
        this.value = value;
    }

}
